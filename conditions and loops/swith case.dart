void main(){
   var dayOfWeek = 5;
if (dayOfWeek == 1) {
        print("Hari ini Minggu.");
  }
else if (dayOfWeek == 2) {
       print("Hari ini Senin.");
     }
else if (dayOfWeek == 3) {
      print("Hari ini Selasa.");
     }
else if (dayOfWeek == 4) {
        print("Hari ini  Rabu.");
     }
else if (dayOfWeek == 5) {
        print("Hari ini Kamis.");
   }
else if (dayOfWeek == 6) {
        print("Hari ini Jumat.");
    }
else if (dayOfWeek == 7) {
        print("Hari ini Sabtu.");
}else{
        print("Invalid Hari.");
     }
}